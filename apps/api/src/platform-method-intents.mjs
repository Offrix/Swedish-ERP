export const CRITICAL_DOMAIN_METHOD_INTENTS = RAW_CRITICAL_DOMAIN_METHOD_INTENTS;

export function resolveCriticalDomainMethodIntent(domainKey, methodName) {
  const domainIntents = CRITICAL_DOMAIN_METHOD_INTENTS[domainKey];
  if (!domainIntents) {
    throw new Error(`No critical domain method intents registered for domain "${domainKey}".`);
  }
  const intent = domainIntents[methodName];
  if (intent !== "read" && intent !== "write") {
    throw new Error(`No critical domain method intent registered for ${domainKey}.${methodName}.`);
  }
  return intent;
}

function resolveDomainMethodNames(registration, domains) {
  const domain = domains?.[registration.domainKey];
  if (!domain || typeof domain !== "object") {
    return registration.capabilities;
  }
  return Object.keys(domain)
    .filter((methodName) => typeof domain[methodName] === "function")
    .sort();
}

export function listCriticalDomainMethodIntents(domainRegistry = [], domains = null) {
  return domainRegistry.flatMap((registration) => {
    const domainIntents = CRITICAL_DOMAIN_METHOD_INTENTS[registration.domainKey] || {};
    return resolveDomainMethodNames(registration, domains)
      .filter((methodName) => typeof domainIntents[methodName] === "string")
      .map((methodName) => Object.freeze({
        domainKey: registration.domainKey,
        methodName,
        intent: domainIntents[methodName]
      }));
  });
}

export function assertCriticalDomainMethodIntentCoverage(domainRegistry = [], domains = null) {
  const missing = [];
  for (const registration of domainRegistry) {
    const domainIntents = CRITICAL_DOMAIN_METHOD_INTENTS[registration.domainKey];
    if (!domainIntents) {
      missing.push(`${registration.domainKey}.*`);
      continue;
    }
    for (const methodName of resolveDomainMethodNames(registration, domains)) {
      if (typeof domainIntents[methodName] !== "string") {
        missing.push(`${registration.domainKey}.${methodName}`);
      }
    }
  }
  if (missing.length > 0) {
    throw new Error(`Missing critical domain method intents: ${missing.join(", ")}`);
  }
  return true;
}
